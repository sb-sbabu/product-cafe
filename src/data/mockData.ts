export function getResourceById(id: string): Resource | undefined {
    return mockResources.find(r => r.id === id);
}

export function getFAQById(id: string): FAQ | undefined {
    return mockFAQs.find(f => f.id === id);
}

export function getPersonById(id: string): Person | undefined {
    return mockPeople.find(p => p.id === id);
}

export function searchResources(query: string, filters?: { category?: string; pillar?: string }): Resource[] {
    const lowerQuery = query.toLowerCase();

    return mockResources.filter(resource => {
        // Apply text search
        const matchesQuery =
            resource.title.toLowerCase().includes(lowerQuery) ||
            resource.description.toLowerCase().includes(lowerQuery) ||
            resource.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
            resource.searchKeywords.some(kw => kw.toLowerCase().includes(lowerQuery)) ||
            resource.synonyms.some(syn => syn.toLowerCase().includes(lowerQuery));

        if (!matchesQuery) return false;

        // Apply filters
        if (filters?.category && resource.category !== filters.category) return false;
        if (filters?.pillar && resource.pillar !== filters.pillar) return false;

        return true;
    });
}

export function searchFAQs(query: string): FAQ[] {
    const lowerQuery = query.toLowerCase();

    return mockFAQs.filter(faq => {
        return (
            faq.question.toLowerCase().includes(lowerQuery) ||
            faq.alternateQuestions.some(q => q.toLowerCase().includes(lowerQuery)) ||
            faq.answerSummary.toLowerCase().includes(lowerQuery) ||
            faq.tags.some(tag => tag.toLowerCase().includes(lowerQuery))
        );
    });
}

export function searchPeople(query: string): Person[] {
    const lowerQuery = query.toLowerCase();

    return mockPeople.filter(person => {
        return (
            person.displayName.toLowerCase().includes(lowerQuery) ||
            person.title.toLowerCase().includes(lowerQuery) ||
            person.team.toLowerCase().includes(lowerQuery) ||
            person.expertiseAreas.some(area => area.toLowerCase().includes(lowerQuery)) ||
            person.canHelpWith.some(topic => topic.toLowerCase().includes(lowerQuery))
        );
    });
}

export function getFeaturedResources(): Resource[] {
    return mockResources.filter(r => r.isFeatured && !r.isArchived);
}

export function getResourcesByCategory(category: string): Resource[] {
    return mockResources.filter(r => r.category === category && !r.isArchived);
}

export function getResourcesByPillar(pillar: string): Resource[] {
    return mockResources.filter(r => r.pillar === pillar && !r.isArchived);
}
